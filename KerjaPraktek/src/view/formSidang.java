    private void tblSidangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSidangMouseClicked
        // TODO add your handling code here:
        int row = tblSidang.getSelectedRow();
        
        tf_idsidang.setText(tblSidang.getValueAt(row, 0).toString());
        try{
          DefaultTableModel model = (DefaultTableModel)tblSidang.getModel();
          int index = tblSidang.getSelectedRow();
          Date tgl = new SimpleDateFormat("yyyy-MM-dd").parse((String)model.getValueAt(row, 1));
          dp_tggl.setDate(tgl);
        } catch(ParseException ex){
            Logger.getLogger(formSidang.class.getName()).log(Level.SEVERE, null, ex);
        }
        tf_ruangan.setText(tblSidang.getValueAt(row, 2).toString());
        tf_idKp.setText(tblSidang.getValueAt(row, 3).toString());
        btn_edit.setEnabled(true);
        btn_delete.setEnabled(true);
        btn_simpan.setEnabled(false);
    }//GEN-LAST:event_tblSidangMouseClicked

