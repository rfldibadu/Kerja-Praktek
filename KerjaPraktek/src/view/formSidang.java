    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        // TODO add your handling code here:
        Sidang sdg = new Sidang();
        SimpleDateFormat dFormat = new SimpleDateFormat("yyyy-MM-dd");
        String date = dFormat.format(dp_tggl.getDate());
        
        sdg.setIdsidang(tf_idsidang.getText().toString());
        sdg.setTanggal(date);
        sdg.setRuangan(tf_ruangan.getText().toString());
        sdg.setIdkp(tf_idKp.getText().toString());
        
        ExecuteSidang es = new ExecuteSidang();
        String Hasil = es.InsertSidang(sdg);
        JOptionPane.showMessageDialog(rootPane, Hasil);
        displayall();

    }//GEN-LAST:event_btn_simpanActionPerformed

    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
        // TODO add your handling code here:
        Sidang sdg = new Sidang();
        SimpleDateFormat dFormat = new SimpleDateFormat("yyyy-MM-dd");
        String date = dFormat.format(dp_tggl.getDate());
//        mengambil nilai dari komponen
        sdg.setIdsidang(tf_idsidang.getText().toString());
        sdg.setTanggal(date);
        sdg.setRuangan(tf_ruangan.getText().toString());
        sdg.setIdkp(tf_idKp.getText().toString());
        
        ExecuteSidang em = new ExecuteSidang();
        String Hasil = em.UpdateSidang(sdg);
        JOptionPane.showMessageDialog(rootPane, Hasil);
        if(Hasil=="Berhasil"){
            tf_idsidang.setText("");
            tf_ruangan.setText("");
            tf_idKp.setText("");
            displayall();
        }
        btn_edit.setEnabled(false);
        btn_delete.setEnabled(false);
        btn_simpan.setEnabled(true);
    }//GEN-LAST:event_btn_editActionPerformed

    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        // TODO add your handling code here:
        int column= tblSidang.getSelectedColumn();
        int row = tblSidang.getSelectedRow();
        if(column>=0){
            int confirm = JOptionPane.showConfirmDialog(rootPane, "Are You Sure?");
            if (confirm==0){
                System.out.println("Konfirmasi: "+confirm);
                Object ID_Sidang = tblSidang.getModel().getValueAt(row, 0);
                ExecuteSidang es = new ExecuteSidang();
                es.DeleteSidang((String) ID_Sidang);
                JOptionPane.showMessageDialog(rootPane, "Deleted");
                
                tf_idsidang.setText("");
                tf_ruangan.setText("");
                tf_idKp.setText("");
               
            } else if (confirm==1){
                tf_idsidang.setText("");
                tf_ruangan.setText("");
                tf_idKp.setText("");
                displayall();
            }
            displayall();
        }else{
            JOptionPane.showMessageDialog(rootPane, "Silahkan Pilih Data");
        }
        btn_edit.setEnabled(false);
        btn_delete.setEnabled(false);
        btn_simpan.setEnabled(true);
    }//GEN-LAST:event_btn_deleteActionPerformed
    public static Date getTglFromTbl(JTable table, int kolom){
        JTable tabel = table;
        String str_tgl = String.valueOf(tabel.getValueAt(tabel.getSelectedRow(), kolom));
        Date tanggal = null;
        try {
            Date tgl = new SimpleDateFormat("yyyy-MM-dd").parse(str_tgl);
        } catch (ParseException ex) {
            Logger.getLogger(formSidang.class.getName()).log(Level.SEVERE, null, ex);
        }
        return tanggal;
    }
